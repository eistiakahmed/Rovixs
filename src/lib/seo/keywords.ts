// Helper Functions

// Get keywords for a specific service
export const getServiceKeywords = (serviceSlug: string): string[] => {
  return SERVICE_KEYWORDS[serviceSlug] || [];
};

// Get technology-specific keywords
export const getTechKeywords = (technology: string): string[] => {
  return TECH_KEYWORDS[technology] || [];
};

// Get blog category keywords
export const getBlogKeywords = (category: string): string[] => {
  return BLOG_KEYWORDS[category] || [];
};

// Generate meta keywords from content
export const generateMetaKeywords = (...keywordArrays: string[][]): string[] => {
  return [...new Set(keywordArrays.flat())];
};

// Filter keywords by difficulty
export const filterByDifficulty = (
  maxDifficulty: number,
  keywords: string[]
): string[] => {
  return keywords.filter((keyword) => {
    const metric = KEYWORD_METRICS[keyword];
    return metric ? metric.difficulty <= maxDifficulty : true;
  });
};

// Filter keywords by volume
export const filterByVolume = (minVolume: number, keywords: string[]): string[] => {
  return keywords.filter((keyword) => {
    const metric = KEYWORD_METRICS[keyword];
    return metric ? metric.volume >= minVolume : true;
  });
};

// Get high-opportunity keywords (lower difficulty, decent volume)
export const getOpportunityKeywords = (): string[] => {
  return Object.entries(KEYWORD_METRICS)
    .filter(([_, metric]) => metric.difficulty < 70 && metric.volume > 1000)
    .map(([keyword]) => keyword);
};

// Generate location-specific keywords
export const generateLocationKeywords = (locations: string[]): string[] => {
  const baseKeywords = [
    'software development company',
    'custom software development',
    'mobile app development',
    'web development company',
    'software developers',
  ];

  return locations.flatMap((location) =>
    baseKeywords.map((keyword) => `${keyword} ${location}`)
  );
};

